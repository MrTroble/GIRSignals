package eu.gir.girsignals.models;

import java.util.List;

public class Models {

    private List<TextureStats> textures;
    private float x;
    private float y;
    private float z;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }

    public List<TextureStats> getTexture() {
        return textures;
    }
}
